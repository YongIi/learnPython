# 常见的数据类型：int float bool str
# 十进制是默认进制
# 二进制以0b开头
# 八进制以0o开头
# 十六进制以0x开头

n1 = 90
n2 = -69
print(n1, type(n1))
print(n2, type(n2))

# 整数可以表示为二进制、十进制、八进制、十六进制
print("十进制", 118)
print("二进制", 0b101010)  # 默认输出的是十进制
print("八进制", 0o176)
print("十六进制", 0x1EAF)
