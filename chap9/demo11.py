# 字符串的精度与宽度
print("———采用%———")

print('%d' % 99)
print('%10d' % 99)  # 10表示宽度

print('%.3f' % 3.1415926)  # .3表示保留3位小数

print('%10.3f' % 3.1415926)  # 10表示宽度  .3表示保留3位小数

print("———采用{}———")

print('{0}'.format(3.1415926))

print('{0:.3}'.format(3.1415926))  # .3表示一共保留3位  0代表占位符的顺序
print('{0:.3f}'.format(3.1415926))  # .3f表示小数点后保留3位

print('{0:10.3f}'.format(3.1415926))  # 同时设置宽度和精度 10表示宽度 .3f表示小数点后保留3位